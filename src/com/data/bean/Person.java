package com.data.bean;

public class Person {
	private String studentname;
	private String sex;
	private String studentclass;
	
	private String studentnumber;
	private String state;
	private String email;
	private String indirectingtime;
	private String activisttime;
	private String developementtime;
	private String probationarytime;
	private String fullmembertime;
	public String getStudentname() {
		return studentname;
	}
	public void setStudentname(String studentname) {
		this.studentname = studentname;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getStudentclass() {
		return studentclass;
	}
	public void setStudentclass(String studentclass) {
		this.studentclass = studentclass;
	}
	public String getStudentnumber() {
		return studentnumber;
	}
	public void setStudentnumber(String studentnumber) {
		this.studentnumber = studentnumber;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getIndirectingtime() {
		return indirectingtime;
	}
	public void setIndirectingtime(String indirectingtime) {
		this.indirectingtime = indirectingtime;
	}
	public String getActivisttime() {
		return activisttime;
	}
	public void setActivisttime(String activisttime) {
		this.activisttime = activisttime;
	}
	public String getDevelopementtime() {
		return developementtime;
	}
	public void setDevelopementtime(String developementtime) {
		this.developementtime = developementtime;
	}
	public String getProbationarytime() {
		return probationarytime;
	}
	public void setProbationarytime(String probationarytime) {
		this.probationarytime = probationarytime;
	}
	public String getFullmembertime() {
		return fullmembertime;
	}
	public void setFullmembertime(String fullmembertime) {
		this.fullmembertime = fullmembertime;
	}
}
